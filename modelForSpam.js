// Подключение библиотеки Brain.js
const brain = require('brain.js');

// Подготовка обучающего набора данных (только вредители)
const maliciousData = [
  { input: "вредитель1", output: [1] },
  { input: "вредитель2", output: [1] },
  // Добавьте другие вредоносные примеры
];

// Формирование набора данных для не вредителей (может быть пустым, так как не входит в обучение)
const nonMaliciousData = [
  { input: "не вредитель1", output: [0] },
  { input: "не вредитель2", output: [0] },
  // Добавьте другие примеры не вредителей, если необходимо
];

// Создание экземпляра нейронной сети
const net = new brain.NeuralNetwork();

// Обучение нейронной сети только на вредителях
net.train(maliciousData, {
  iterations: 2000, // количество итераций обучения
  errorThresh: 0.005, // порог ошибки
  log: true, // вывод прогресса в консоль
  logPeriod: 100 // частота вывода прогресса
});

// Пример использования обученной нейронной сети для предсказания
const inputToPredict = "вредитель1";
const output = net.run(inputToPredict); // ожидаемый результат: [1], т.е. вредитель

console.log(output); // Вывод результата предсказания
